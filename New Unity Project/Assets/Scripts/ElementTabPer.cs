
public class ElementTabPer
{
    private string name;
    private string simbol;
    private int nroatomico;
    private string confElectronica;
    private float pesoAtomico;

    #region Set & Get
    public string Name
    {
        get
        {
            return name;
        }

        set
        {
            name = value;
        }
    }

    public string Simbol
    {
        get
        {
            return simbol;
        }

        set
        {
            simbol = value;
        }
    }

    public int Nroatomico
    {
        get
        {
            return nroatomico;
        }

        set
        {
            nroatomico = value;
        }
    }

    public string ConfElectronica
    {
        get
        {
            return confElectronica;
        }

        set
        {
            confElectronica = value;
        }
    }

    public float PesoAtomico
    {
        get
        {
            return pesoAtomico;
        }

        set
        {
            pesoAtomico = value;
        }
    }

    #endregion



}

